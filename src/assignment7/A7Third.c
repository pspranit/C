/*//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
 Accept distance in kilometer and convert it into meter.(1km = 1000 meter)

// i/p : 5
// o/p : 5000

// i/p : 12
// o/p : 12000
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/

#include<stdio.h>
#define MTR 1000

int KMtoMeter(int iNo)
{
	int iMeter = 0;

	iMeter = iNo * MTR;
	
	return iMeter;
}
int main()
{
	int iValue = 0;
	int iRet = 0;

	printf("Enter Kilometer:\n");
	scanf("%d",&iValue);

	iRet = KMtoMeter(iValue);
	printf("%d",iRet);

	return 0;
}
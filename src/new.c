#include <stdio.h>

int main()
{
    int n; // Declare variable 'n'
    
    // n=1; <initial_statement> : initialze 'n' with 1
    // n<=100; <conditional_expression> : repeate for loop till 'n' is less or equal 100
    // n=n+1; <increment_statement> : increment 'n' by 1
    
    for(n=1; n<=100; n++) {
        printf("%d ", n); // Display 'n'
    }

    return 0;
}

    